CREATE TABLE IF NOT EXISTS characters
(
    characters_id INT NOT NULL AUTO_INCREMENT,
    PRIMARY KEY (characters_id),
    name VARCHAR(255) NOT NULL,
    passive_effects INT NOT NULL,
    FOREIGN KEY (passive_effects) REFERENCES skills (skills_id),
    skill1 INT NOT NULL,
    FOREIGN KEY (skill1) REFERENCES skills (skills_id),
    skill2 INT NOT NULL,
    FOREIGN KEY (skill2) REFERENCES skills (skills_id),
    skill3 INT NOT NULL,
    FOREIGN KEY (skill3) REFERENCES skills (skills_id),
    skill4 INT NOT NULL,
    FOREIGN KEY (skill4) REFERENCES skills (skills_id),
    max_hp DOUBLE NOT NULL,
    max_mp DOUBLE NOT NULL,
    shield DOUBLE NOT NULL,
    defense DOUBLE NOT NULL,
    fire_resistance DOUBLE NOT NULL,
    chill_resistance DOUBLE NOT NULL,
    electric_resistance DOUBLE NOT NULL,
    toxin_resistance DOUBLE NOT NULL,
    fire_skill_power DOUBLE NOT NULL,
    chill_skill_power DOUBLE NOT NULL,
    electric_skill_power DOUBLE NOT NULL,
    toxin_skill_power DOUBLE NOT NULL,
    fusion_skill_power_modifier DOUBLE NOT NULL,
    singular_skill_power_modifier DOUBLE NOT NULL,
    dimension_skill_power_modifier DOUBLE NOT NULL,
    tech_skill_power_modifier DOUBLE NOT NULL,
    skill_critical_hit_rate DOUBLE NOT NULL,
    skill_critical_hit_damage DOUBLE NOT NULL,
    skill_power DOUBLE NOT NULL,
    all_attribute_resistances DOUBLE NOT NULL,
    hp_regen_modifier DOUBLE NOT NULL,
    mp_regen_modifier DOUBLE NOT NULL,
    hp_heal DOUBLE NOT NULL,
    skill_cooldown DOUBLE NOT NULL,
    skill_cost DOUBLE NOT NULL,
    skill_effect_range DOUBLE NOT NULL,
    skill_duration DOUBLE NOT NULL,
    skill_speed DOUBLE NOT NULL,
    critical_hit_resistance DOUBLE NOT NULL,
    skill_power_modifier DOUBLE NOT NULL
);