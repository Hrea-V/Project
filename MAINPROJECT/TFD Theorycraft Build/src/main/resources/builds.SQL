Create TABLE IF NOT EXISTS builds
(
    builds_id INT NOT NULL AUTO_INCREMENT,
    PRIMARY KEY (builds_id),
    character_id INT NOT NULL,
    FOREIGN KEY (character_id) REFERENCES characters (characters_id),
    build_name VARCHAR(255) NOT NULL,
    max_capacity INT NOT NULL,
    current_capacity INT NOT NULL,
    mod1 INT,
    FOREIGN KEY (mod1) REFERENCES mods (mods_id),
    mod2 INT,
    FOREIGN KEY (mod2) REFERENCES mods (mods_id),
    mod3 INT,
    FOREIGN KEY (mod3) REFERENCES mods (mods_id),
    mod4 INT,
    FOREIGN KEY (mod4) REFERENCES mods (mods_id),
    mod5 INT,
    FOREIGN KEY (mod5) REFERENCES mods (mods_id),
    mod6 INT,
    FOREIGN KEY (mod6) REFERENCES mods (mods_id),
    mod7 INT,
    FOREIGN KEY (mod7) REFERENCES mods (mods_id),
    mod8 INT,
    FOREIGN KEY (mod8) REFERENCES mods (mods_id)
);